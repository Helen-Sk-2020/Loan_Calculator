s = input()
e = input()
if s == '1/2' and e == '-1/3':
    print("Strange Quark")
elif s == '1/2' and e == '2/3':
    print("Charm Quark")
elif s == '1/2' and e == '-1':
    print("Electron Lepton")
elif s == '1/2' and e == '0':
    print("Neutrino Lepton")
elif s == '1' and e == '0':
    print("Photon Boson")
